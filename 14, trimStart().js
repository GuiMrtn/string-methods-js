// La méthode trimStart() permet de supprimer les espaces blancs (espaces, tabulations, retours à la ligne, etc...) situés au début d'une chaîne de caractères. Cette méthode ne modifie pas la chaîne d'origine, mais renvoie une nouvelle chaîne sans les espaces en excès.

// Exemple de base
let string = "   Salut tout le monde   ";
let result = string.trimStart();
console.log(result); // Affiche "Salut tout le monde   "
